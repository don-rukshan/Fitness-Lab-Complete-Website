      <h2 class="pb-4">One Tip To Transform Your Bench Press </h2>

      <h5 class="pb-4"> Every lifter wants a big chest and big numbers on their bench. Use this one tip to get both!</h5>

      <div class="col-lg-12 pb-5"><img class="img-fluid" src="img/work6.png"></div>

      <p class="text-justify">There are two things that almost every lifter in the weight room cares about: growing their chest and increasing their bench. Fortunately, if you focus on one, you can usually improve the other.</p>

      <p class="text-justify">Although strength is usually a bigger priority for powerlifters, bodybuilders can also benefit from focusing on strength since heavier weight can lead to growth. This chest-building tip is a technique powerlifters use to increase strength, but its ability to add thickness and mass to your chest is exactly why you should add it to your routine.</p>

      <h4 class="pb-4 text-left">Training Tip: Add Chains</h4>

      <p class="text-justify">Have you seen the videos on YouTube of lifters adding chains to a bar that already has a lot of weight on it? It isn't just to look cool or hardcore, those chains are providing a major benefit. They help you improve your strength, especially if you need to work on that sticking point in your bench.</p>


      <h5 class="text-left">How It Works</h5>

      <p class="text-justify">Chains are a unique form of resistance and a great way to add intensity to your bench. When you loop the chains over the ends of the barbell, some of the links are still on the ground and therefore don't add any weight to the bar. As you press up, those links lift off the ground, joining the rest of the weight you're pressing. When you lower the bar back down, the links of the chain gather on the floor and the weight lightens again.</p>

      <div class="col-lg-12 pb-5"><img class="img-fluid" src="img/work66.png"></div>

      <p class="text-justify">What does this mean for your bench? Let's say you have 135 pounds on the bar and 40 pounds of chains. That's 175 pounds total. As you lower the bar, you might only have around 150 pounds in your hands. That's important because your pecs, shoulders, and rotator cuffs are in a compromising position at the bottom of your bench, and less resistance at the bottom means less chance of injury.</p>

      <p class="text-justify pb-4">Once you press back up, that weight increases until you have the full 175 pounds at the top. This means your muscles have to adapt to the increase in resistance throughout the movement to complete the rep. That added challenge is what makes this so effective.</p>


      <h5 class="text-left">Why It Works</h5>

      <p class="text-justify">That change in resistance isn't just working on your strength. The muscle fibers are going to respond differently because the weight on the fibers at the stretched position is less than the weight you have when you contract those fibers while pressing. Those pecs will be denser and thicker as a result of this technique, and your strength will improve, too.</p>

      <h5 class="text-left">How to Do It</h5>

      <p class="text-justify">The first thing you should do is determine what the chains weigh. Put them on the scale so you know what you're going to be working with. You don't want to use random chains and have them turn out to be heavier than you thought.</p>



      <p class="text-justify">Don't simply wrap the chains around the bar and think you're good. There is a calculated way you need to apply those chains if you want to reap the greatest benefit. Namely, you want the chain to be completely off the floor when the bar is at the top of the lift, and you want as much of the chain to be on the floor as possible when the bar is on your chest.</p>

      <div class="col-lg-12 pb-4"><img class="img-fluid" src="img/work666.png"></div>

      <p class="text-justify pb-4">You can use a feeder chain on the bar and wrap your heavier chains on those. There are also specific straps or collars that you can apply to the bar that are designed for adding chains.</p>

      <h5 class="text-left">How to Get Started</h5>

      <p class="text-justify">If you've never done this before, work with only the bar and chains until you're more comfortable with how it feels. Once you start adding weight, use no more than 60 percent of your one-rep max (1RM), which should include the chain weight. For example, if your 1RM is 300 pounds, and you're using 40 pounds of chains, you only need 140 pounds on the bar for this type of training to be effective.</p>

      <p class="text-justify">If you are a powerlifter prepping for competition, you might do sets of doubles or triples with more weight, but I don't advise this if you're new to chain training. If all you're looking to do is increase your bench and add size to your chest, perform 3 sets of 8 reps starting out with that 60 percent of your 1RM. After that, either bench without the chains or move on to another exercise.</p>

